    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String ID=jTextField1.getText();
        try{
            Connection con= ConnectionProvider.getCon();
            Statement st=con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
            ResultSet rs=st.executeQuery("select * from student where id='"+ID+"'");
            if (rs.first()){
                jTextField10.setText(rs.getString(2));
                jTextField3.setText(rs.getString(5));
                jTextField6.setText(rs.getString(3));
                jTextField9.setText(rs.getString(4));
                jTextArea1.setText(rs.getString(6));
                jTextField4.setText(rs.getString(7));
                jTextField5.setText(rs.getString(8));
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }

        //search

    }//GEN-LAST:event_jButton1ActionPerformed

